# Arithmetic Operators
a = 5
b = 2

print(a+b)
print(a-b)
print(a*b)
print(a/b) # float values
print(a//b) # int values
print(a%b) # reminder
print(a**b) # power

# Relational Operator

print(a==b)
# == != > < >= <=  this return always boolean value

# Assigment operator ( = )

a = b
print(a)
# += *= -= /= **= are the Assigment operators

